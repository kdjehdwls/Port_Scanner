    print("\nNot_Detected_service port")
    print(', '.join(map(str, Not_Detected_service)))
    
    # 감지된 서비스, 닫힌 서비스, 감지되지 않은 서비스의 총 개수를 출력합니다.
    print(f"\nTotal open services: {len(Detected_service)}")
