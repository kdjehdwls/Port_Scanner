# 사전(dictionary) 형태의 데이터를 출력하는 함수
def print_dict(dictionary):
    # 사전의 각 항목(키와 값)에 대해 반복합니다.
    for key, value in dictionary.items():
        # 각 키와 값을 콜론과 함께 출력합니다.
        print(f"{key}: {value}")

# 서비스 스캔 결과를 출력하는 함수
def service_result_printing(Detected_service, Closed_service, Not_Detected_service):
    # 감지된 서비스, 닫힌 서비스, 감지되지 않은 서비스 목록을 출력합니다.
    print("\nDetected_service:")
